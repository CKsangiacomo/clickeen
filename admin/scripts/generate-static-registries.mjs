#!/usr/bin/env node
/* eslint-disable no-console */
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptPath = fileURLToPath(import.meta.url);
const adminRoot = path.resolve(path.dirname(scriptPath), '..');
const repoRoot = path.resolve(adminRoot, '..');
const dataRoot = path.join(adminRoot, 'src', 'data');

const toPosix = (value) => value.replace(/\\/g, '/');

const modulePathFromData = (targetPath) => {
  const rel = toPosix(path.relative(dataRoot, targetPath));
  return rel.startsWith('.') ? rel : `./${rel}`;
};

const keyFromData = (targetPath) => toPosix(path.relative(dataRoot, targetPath));

const jsString = (value) => JSON.stringify(value);

async function walkFiles(root, predicate) {
  const output = [];
  const entries = await fs.readdir(root, { withFileTypes: true });
  for (const entry of entries) {
    const fullPath = path.join(root, entry.name);
    if (entry.isDirectory()) {
      output.push(...(await walkFiles(fullPath, predicate)));
      continue;
    }
    if (entry.isFile() && predicate(fullPath)) output.push(fullPath);
  }
  return output.sort((a, b) => a.localeCompare(b));
}

async function writeFileIfChanged(filePath, contents) {
  try {
    const current = await fs.readFile(filePath, 'utf8');
    if (current === contents) return;
  } catch {
    // Missing generated files are expected on a fresh checkout.
  }
  await fs.writeFile(filePath, contents, 'utf8');
}

async function generateShowcaseRegistry() {
  const files = await walkFiles(path.join(adminRoot, 'src', 'html'), (filePath) => filePath.endsWith('.html'));
  const imports = [];
  const entries = [];

  files.forEach((filePath, index) => {
    const binding = `html${index}`;
    const importPath = modulePathFromData(filePath);
    imports.push(`import ${binding} from ${jsString(`${importPath}?raw`)};`);
    entries.push(`  ${jsString(keyFromData(filePath))}: ${binding},`);
  });

  await writeFileIfChanged(
    path.join(dataRoot, 'showcase.generated.ts'),
    [
      '/* Generated by admin/scripts/generate-static-registries.mjs. */',
      ...imports,
      '',
      'export const staticShowcaseModules: Record<string, string> = {',
      ...entries,
      '};',
      '',
    ].join('\n'),
  );
}

async function generateIconRegistry() {
  const files = await walkFiles(path.join(repoRoot, 'dieter', 'icons', 'svg'), (filePath) => filePath.endsWith('.svg'));
  const imports = [];
  const entries = [];

  files.forEach((filePath, index) => {
    const binding = `icon${index}`;
    const importPath = modulePathFromData(filePath);
    imports.push(`import ${binding} from ${jsString(`${importPath}?raw`)};`);
    entries.push(`  ${jsString(keyFromData(filePath))}: ${binding},`);
  });

  await writeFileIfChanged(
    path.join(dataRoot, 'icons.generated.ts'),
    [
      '/* Generated by admin/scripts/generate-static-registries.mjs. */',
      ...imports,
      '',
      'export const iconModules: Record<string, string> = {',
      ...entries,
      '};',
      '',
    ].join('\n'),
  );
}

async function generateComponentRegistry() {
  const componentRoot = path.join(repoRoot, 'dieter', 'components');
  const specFiles = await walkFiles(componentRoot, (filePath) => filePath.endsWith('.spec.json'));
  const templateFiles = await walkFiles(componentRoot, (filePath) => filePath.endsWith('.html'));
  const cssFiles = await walkFiles(componentRoot, (filePath) => filePath.endsWith('.css'));
  const imports = ["import type { ComponentSpec } from './componentTypes';"];
  const specEntries = [];
  const templateEntries = [];
  const cssEntries = [];

  specFiles.forEach((filePath, index) => {
    const binding = `spec${index}`;
    imports.push(`import ${binding} from ${jsString(modulePathFromData(filePath))};`);
    specEntries.push(`  ${jsString(keyFromData(filePath))}: ${binding} as ComponentSpec,`);
  });

  templateFiles.forEach((filePath, index) => {
    const binding = `template${index}`;
    imports.push(`import ${binding} from ${jsString(`${modulePathFromData(filePath)}?raw`)};`);
    templateEntries.push(`  ${jsString(keyFromData(filePath))}: ${binding},`);
  });

  cssFiles.forEach((filePath, index) => {
    const binding = `css${index}`;
    imports.push(`import ${binding} from ${jsString(`${modulePathFromData(filePath)}?raw`)};`);
    cssEntries.push(`  ${jsString(keyFromData(filePath))}: ${binding},`);
  });

  await writeFileIfChanged(
    path.join(dataRoot, 'componentRegistry.generated.ts'),
    [
      '/* Generated by admin/scripts/generate-static-registries.mjs. */',
      ...imports,
      '',
      'export const specModules: Record<string, ComponentSpec> = {',
      ...specEntries,
      '};',
      '',
      'export const templateModules: Record<string, string> = {',
      ...templateEntries,
      '};',
      '',
      'export const cssModules: Record<string, string> = {',
      ...cssEntries,
      '};',
      '',
    ].join('\n'),
  );
}

export async function generateStaticRegistries() {
  await fs.mkdir(dataRoot, { recursive: true });
  await generateShowcaseRegistry();
  await generateIconRegistry();
  await generateComponentRegistry();
}

if (process.argv[1] === scriptPath) {
  generateStaticRegistries().catch((error) => {
    console.error('[devstudio:generate-static-registries] Failed.', error instanceof Error ? error.message : String(error));
    process.exit(1);
  });
}
