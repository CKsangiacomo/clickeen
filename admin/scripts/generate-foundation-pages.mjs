export async function generateFoundationPages() {
  await fs.mkdir(foundationsRoot, { recursive: true });
  const colorCount = await generateColorsPage();
  const iconCount = await generateIconsPage();
  await generateTypographyPage();
  return { colorCount, iconCount };
}

if (process.argv[1] === scriptPath) {
  generateFoundationPages()
    .then(({ colorCount, iconCount }) => {
      console.log(`[generate-foundation-pages] Wrote colors (${colorCount}) and icons (${iconCount}) pages.`);
    })
    .catch((error) => {
      console.error('[generate-foundation-pages] Failed.', error instanceof Error ? error.message : String(error));
      process.exit(1);
    });
}
