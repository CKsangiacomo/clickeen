#!/usr/bin/env node
/* eslint-disable no-console */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const widgetsRoot = path.join(repoRoot, "tokyo/product/widgets");
const outputPath = path.join(
  repoRoot,
  "tokyo-worker/src/generated/widget-definition-sources.ts",
);
const checkOnly = process.argv.includes("--check");

function discoverWidgetTypes() {
  return fs
    .readdirSync(widgetsRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .filter((name) => name !== "shared")
    .filter((name) => fs.existsSync(path.join(widgetsRoot, name, "spec.json")))
    .sort((a, b) => a.localeCompare(b));
}

function variableName(widgetType, suffix) {
  const safe = widgetType.replace(/[^a-zA-Z0-9]+/g, "_");
  return `${safe}${suffix}`;
}

function buildSource(widgetTypes) {
  const imports = widgetTypes.flatMap((widgetType) => {
    const prefix = `../../../tokyo/product/widgets/${widgetType}`;
    return [
      `import ${variableName(widgetType, "Catalog")} from "${prefix}/catalog.json";`,
      `import ${variableName(widgetType, "EditableFields")} from "${prefix}/editable-fields.json";`,
      `import ${variableName(widgetType, "Spec")} from "${prefix}/spec.json";`,
    ];
  });

  const entries = widgetTypes
    .map(
      (widgetType) => `  {
    widgetType: "${widgetType}",
    spec: ${variableName(widgetType, "Spec")},
    catalog: ${variableName(widgetType, "Catalog")},
    editableFields: ${variableName(widgetType, "EditableFields")},
  },`,
    )
    .join("\n");

  return `// Generated by scripts/generate-widget-definition-sources.mjs. Do not edit manually.
// This is a source index for bundling Tokyo widget definitions; it is not product state.

${imports.join("\n")}

export type WidgetDefinitionSource = {
  widgetType: string;
  spec: unknown;
  catalog: unknown;
  editableFields: unknown;
};

export const WIDGET_DEFINITION_SOURCES = [
${entries}
] as const satisfies readonly WidgetDefinitionSource[];
`;
}

const nextSource = buildSource(discoverWidgetTypes());

if (checkOnly) {
  const currentSource = fs.existsSync(outputPath)
    ? fs.readFileSync(outputPath, "utf8")
    : "";
  if (currentSource !== nextSource) {
    console.error(
      `[generate-widget-definition-sources] ${path.relative(
        repoRoot,
        outputPath,
      )} is out of date. Run: node scripts/generate-widget-definition-sources.mjs`,
    );
    process.exit(1);
  }
  console.log("[generate-widget-definition-sources] OK");
  process.exit(0);
}

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, nextSource);
console.log(
  `[generate-widget-definition-sources] wrote ${path.relative(repoRoot, outputPath)}`,
);
